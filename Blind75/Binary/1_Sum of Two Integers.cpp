class Solution {
public:
    int getSum(int a, int b) {
        // Loop until there is no carry
        while (b != 0) {
            // Calculate the carry, which is common set bits of a and b, shifted left by one
            int carry = (a & b) << 1;
            // Sum of bits of a and b where at least one of the bits is not set
            a = a ^ b;
            // Assign carry to b, so it can be added in the next iteration
            b = carry;
        }
        // When carry is zero, a contains the sum
        return a;
    }
};

